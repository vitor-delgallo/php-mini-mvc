    <p><strong>Mini-MVC</strong> é um micro framework PHP com estrutura básica de separação de camadas para projetos pequenos e landing pages.</p>

    <ul>
        <li>✅ Suporte a rotas simples (com <code>league/route</code>)</li>
        <li>✅ Sistema de views com templates</li>
        <li>✅ Helpers para sessão, resposta, banco de dados, linguagem, etc.</li>
        <li>✅ Perfeito para hospedagens simples sem necessidade de frameworks grandes</li>
    </ul>

    <p>Esse projeto foi pensado para desenvolvedores que precisam de uma estrutura leve e prática para organizar seus arquivos e reutilizar recursos entre páginas e sistemas pequenos.</p>

    <p>Você pode começar a criar sua landing page agora mesmo com <strong>Mini-MVC</strong>.</p>
